function renderNavPills() {
  const navPills = document.querySelector(".nav-pills");

  itemList.navPills.forEach((pill) => {
    const listItem = document.createElement("li");
    listItem.classList.add("nav-item");

    const link = document.createElement("a");
    link.classList.add("nav-link");
    link.textContent = pill.showName;
    link.setAttribute("data-toggle", "pill");
    link.setAttribute("href", `#${pill.tabName}`);
    link.setAttribute("data-tab", pill.type);

    listItem.appendChild(link);
    navPills.appendChild(listItem);
  });
}
function displayItemList(type) {
  const items = itemList.tabPanes.filter((item) => item.type === type);
  const tabContent = document.querySelector(".tab-content");
  tabContent.innerHTML = "";

  items.forEach((item) => {
    const html = `
        <div class="tab-pane container active col-3" id="${item.id}">        
              <div class="card text-center">
                <img src="${item.imgSrc_jpg}">
                <h4>
                  <b>${item.name}</b>
                </h4>
                <button data-id="${item.id}" data-type="${item.type}" data-name="${item.name}" data-desc="${item.desc}" data-imgsrcjpg="${item.imgSrc_jpg}" data-imgsrcpng="${item.imgSrc_png}" class="changStyle">Thử đồ</button>
              </div>
        </div>
      `;

    tabContent.insertAdjacentHTML("beforeend", html);
  });

  const buttons = document.querySelectorAll(".changStyle");
  buttons.forEach((button) => {
    button.addEventListener("click", function () {
      const itemId = this.getAttribute("data-id");
      const itemType = this.getAttribute("data-type");
      const itemImgSrc = this.getAttribute("data-imgsrcpng");
      if (itemType === "background") {
        changeBackground(itemImgSrc);
      } else if (itemType === "topclothes") {
        changeBikiniTop(itemImgSrc);
      } else if (itemType === "botclothes") {
        changeBikiniBottom(itemImgSrc);
      } else if (itemType === "shoes") {
        changeFeet(itemImgSrc);
      } else if (itemType === "necklaces") {
        changeNecklaces(itemImgSrc);
      } else if (itemType === "handbags") {
        changeHandBags(itemImgSrc);
      } else if (itemType === "hairstyle") {
        changeHair(itemImgSrc);
      }
    });
  });
}

const changeBackground = (imgSrc) => {
  const background = document.querySelector(".background");
  background.style.backgroundImage = `url(${imgSrc})`;
}

const changeBikiniTop = (imgSrc) => {
  const bikinitop = document.querySelector(".bikinitop");

  bikinitop.style.background = `url(${imgSrc})`;
  bikinitop.style.width = "500px";
  bikinitop.style.height = "500px";
  bikinitop.style.position = "absolute";
  bikinitop.style.top = "-9%";
  bikinitop.style.left = "-5%";
  bikinitop.style.zIndex = "3";
  bikinitop.style.transform = "scale(0.5)";
}

